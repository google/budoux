def extract_intent_target(
    issue_text: str,
    client: genai.Client,
    model_name: str = "gemini-3.1-flash-lite",
) -> IntentTarget:
  """Intent Understanding Agent: diagnoses target bug parameters from issue text."""
  prompt = f"""You are an expert BudouX segmentation analyst.
Examine this GitHub bug issue description:
---
{issue_text}
---
Determine the primary problematic phrase and its desired canonical split formatted with '▁'.
If a phrase was mistakenly split internally and should stay unbroken (e.g., 'もはや'), return target_phrase='もはや', expected_split='もはや', is_positive=False.
If a phrase needed a boundary across separate words that was missing or shifted (e.g., 'いよいよ/はじまる'), format expected_split inserting '▁' at the intended break location and set is_positive=True.
"""
  config = genai.types.GenerateContentConfig(
      response_mime_type="application/json",
      response_schema=IntentTarget,
      temperature=0.1,
  )
  resp = client.models.generate_content(
      model=model_name, contents=prompt, config=config)
  return IntentTarget.model_validate_json(resp.text or "")


def generate_oversample_candidates(
    target: IntentTarget,
    client: genai.Client,
    num_candidates: int = 30,
    lang: str = "ja",
    model_name: str = "gemini-3.1-flash-lite",
) -> List[str]:
  """Example Generator Agent: synthesizes diverse natural sentences cleanly across target lang."""
  prompt = f"""You are a skilled corpus synthesizer for target language code '{lang}'.
Generate exactly {num_candidates} highly natural, realistic sentences across language '{lang}' where the phrase '{target.expected_split}' appears cleanly within distinct realistic contexts (e.g., news headlines, casual speech, essays, formal letters).

CRITICAL CONSTRAINTS:
1. Every candidate MUST contain the exact substring '{target.expected_split}' strictly as formatted.
2. Maintain natural grammatical structures and standard Bunsetsu word boundaries across language '{lang}'.
"""
  config = genai.types.GenerateContentConfig(
      response_mime_type="application/json",
      response_schema=CandidateBatch,
      temperature=0.4,
  )
  resp = client.models.generate_content(
      model=model_name, contents=prompt, config=config)
  batch = CandidateBatch.model_validate_json(resp.text or "")
  return batch.candidates


def prune_linguistic_anomalies(
    candidates: List[str],
    client: genai.Client,
    max_keep: int = 15,
    lang: str = "ja",
    model_name: str = "gemini-3.1-flash-lite",
) -> List[str]:
  """Linguistic Expert Agent: polishes boundary alignment across candidates and audits fluency."""
  if not candidates:
    return []

  lines_summary = [f"{i}. {s}" for i, s in enumerate(candidates)]
  prompt = f"""You are an expert linguistic polisher and boundary arbiter for language '{lang}'.
Review these candidate training sentences containing existing boundary splits marked by '▁':
{chr(10).join(lines_summary)}

Your critical mission across three synthesized layers (existing base parser behavior, intent understanding, and linguistic fluency):
1. Evaluate whether the sentence is natural and well-structured across target language '{lang}' (is_fluent). Immediately reject (is_fluent=False) malformed syntax or broken words.
2. For fluent sentences (is_fluent=True), perform precise boundary polishing and return the perfected string in 'corrected_sentence':
   - Keep quotation framing clean without forcing splits directly after opening brackets or before closing quotes (e.g., maintain '「こんにちは」と▁...' without cutting around quotation marks).
   - If an independent vocabulary word or bunsetsu was improperly fused due to base model deficiencies (e.g., '彼の▁才能はもはや誰にも▁...' where words like 'もはや' lack surrounding breaks), restore clean natural bunsetsu gaps around the word ('彼の▁才能は▁もはや▁誰にも▁...').
   - Never alter or remove the canonical splits established inside the targeted phrase itself.
"""
  config = genai.types.GenerateContentConfig(
      response_mime_type="application/json",
      response_schema=EvaluationBatch,
      temperature=0.1,
  )
  resp = client.models.generate_content(
      model=model_name, contents=prompt, config=config)
  eval_batch = EvaluationBatch.model_validate_json(resp.text or "")

  kept: List[str] = []
  for e in eval_batch.evaluations:
    if e.is_fluent:
      kept.append(e.corrected_sentence.strip() or e.sentence)
  if not kept:
    kept = candidates
  return kept[:max_keep]



def run_agentic_synthesis_pipeline(
    input_str: Optional[str] = None,
    issue_id: Optional[str] = None,
    num_candidates: int = 30,
    max_keep: int = 15,
    lang: str = "ja",
    outfile: str = "staging_raw.txt",
    client: Optional[genai.Client] = None,
    parser: Optional[budoux.Parser] = None,
) -> List[str]:
  """Orchestrates Intent Understanding, Oversample Generation, and Expert Pruning."""
  if not parser:
    model_path = os.path.join(
        os.path.dirname(__file__), "..", "budoux", "models", f"{lang}.json")
    with open(model_path, "r", encoding="utf-8") as f:
      parser = budoux.Parser(json.load(f))

  if not client and (issue_id or num_candidates > 0):
    api_key = os.environ.get("GEMINI_API_KEY", "").strip()
    if not api_key:
      raise RuntimeError(
          "GEMINI_API_KEY environment variable not set or empty.")
    client = genai.Client(api_key=api_key)

  # Step 1: Intent & Bug Verification
  if input_str:
    target = parse_direct_input(input_str)
  elif issue_id:
    assert client is not None
    context = fetch_github_issue_context(issue_id)
    target = extract_intent_target(context, client)

  bug_exists = verify_bug_reproduction(target, parser)
  target.is_reproducible = bug_exists
  if not bug_exists:
    print(
        f"[Intent Agent] No bug found: model already parses '{target.expected_split}' correctly."
    )

  # Step 2: Generator Oversampling
  assert client is not None
  raw_candidates = generate_oversample_candidates(
      target, client, num_candidates=num_candidates, lang=lang)
  print(
      f"[Generator Agent] Synthesized {len(raw_candidates)} candidate variations for lang '{lang}'."
  )

  # Step 3: Base Parser Overlay & Linguistic Expert Pruning
  aligned_candidates = align_to_base_parser_splits(raw_candidates, target,
                                                   parser)
  final_candidates = prune_linguistic_anomalies(
      aligned_candidates, client, max_keep=max_keep, lang=lang)
  print(
      f"[Expert Agent] Pruned to {len(final_candidates)} high-confidence rows.")

  output_lines = final_candidates
  if outfile:
    with open(outfile, "w", encoding="utf-8") as f:
      f.write("\n".join(output_lines) + "\n")
    print(f"[Staging] Saved {len(output_lines)} lines directly to {outfile}.")

  return output_lines


def build_parser() -> argparse.ArgumentParser:
  """Constructs command-line flags for agentic candidate sample synthesis."""
  p = argparse.ArgumentParser(description="Agentic candidate sample synthesis.")
  group = p.add_mutually_exclusive_group(required=True)
  group.add_argument(
      "--input", "-i", type=str, help="Target string (e.g. 'いよいよ/はじまる')")
  group.add_argument(
      "--issue", type=str, help="GitHub bug report number or URL ID")
  p.add_argument(
      "--lang",
      type=str,
      default="ja",
      help="Target model language (default: ja)")
  p.add_argument(
      "--num-samples",
      "-n",
      type=int,
      default=30,
      help="Initial generation count")
  p.add_argument(
      "--max-keep", type=int, default=15, help="Pruned final row target")
  p.add_argument(
      "--output",
      "-o",
      type=str,
      default="staging_raw.txt",
      help="Staging destination file")
  return p


def main() -> None:
  args = build_parser().parse_args()
  run_agentic_synthesis_pipeline(
      input_str=args.input,
      issue_id=args.issue,
      num_candidates=args.num_samples,
      max_keep=args.max_keep,
      lang=args.lang,
      outfile=args.output,
  )


if __name__ == "__main__":
  main()
